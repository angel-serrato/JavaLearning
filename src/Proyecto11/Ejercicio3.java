package Proyecto11;

import java.util.Scanner;

/*
 * Desarrollar un programa que muestre la tabla de multiplicar del 5 (del 5 al 50)
 * */

public class Ejercicio3 {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {
            System.out.println( "5 * " + i + " = " + 5 * i );
        }
    }
}
